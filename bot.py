            dp,
            skip_updates=True,  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏—è, –ø–æ–∫–∞ –±–æ—Ç –±—ã–ª offline
