# --- –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω—ã—Ö –æ—à–∏–±–æ–∫ ---
def handle_exception(exc_type, exc_value, exc_traceback):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –Ω–µ–æ–±—Ä–∞–±–æ—Ç–∞–Ω–Ω—ã—Ö –∏—Å–∫–ª—é—á–µ–Ω–∏–π"""
    if issubclass(exc_type, KeyboardInterrupt):
        # –ù–µ –ª–æ–≥–∏—Ä—É–µ–º KeyboardInterrupt
        sys.__excepthook__(exc_type, exc_value, exc_traceback)
        return
    
    logger.critical("‚ùå –ù–ï–û–ë–†–ê–ë–û–¢–ê–ù–ù–û–ï –ò–°–ö–õ–Æ–ß–ï–ù–ò–ï", exc_info=(exc_type, exc_value, exc_traceback))

